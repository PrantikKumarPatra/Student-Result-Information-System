    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
      try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con =DriverManager.getConnection("jdbc:mysql://localhost:3306/student_result","root","");
            String sql="Select * from adminlogin where id=? and password=?";
            PreparedStatement pst=con.prepareStatement(sql);
            pst.setString(1,txt_user.getText());
            pst.setString(2,txt_pass.getText());
            ResultSet rs=pst.executeQuery();
            if(rs.next())
            {
                setVisible(false);
                new adminHome().setVisible(true);
            }
            else
            {
                 JOptionPane.showMessageDialog(null,"Username and Password Do Not Matched");
                 txt_user.setText("");
                 txt_pass.setText("");
            }
            con.close();
        }        
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

